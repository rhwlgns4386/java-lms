create table course
(
    id         bigint generated by default as identity,
    title      varchar(255) not null,
    creator_id bigint       not null,
    created_at timestamp    not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user
(
    id         bigint generated by default as identity,
    user_id    varchar(20) not null,
    password   varchar(20) not null,
    name       varchar(20) not null,
    email      varchar(50),
    created_at timestamp   not null,
    updated_at timestamp,
    primary key (id)
);

create table question
(
    id         bigint generated by default as identity,
    created_at timestamp    not null,
    updated_at timestamp,
    contents   clob,
    deleted    boolean      not null,
    title      varchar(100) not null,
    writer_id  bigint,
    primary key (id)
);

create table answer
(
    id          bigint generated by default as identity,
    created_at  timestamp not null,
    updated_at  timestamp,
    contents    clob,
    deleted     boolean   not null,
    question_id bigint,
    writer_id   bigint,
    primary key (id)
);

create table delete_history
(
    id            bigint not null,
    content_id    bigint,
    content_type  varchar(255),
    created_date  timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table session
(
    id                        bigint generated by default as identity,
    course_id                 bigint       not null,
    title                     varchar(255) not null,
    start_at                  timestamp    not null,
    end_at                    timestamp    not null,
    session_type              varchar(20)  not null,
    session_progress_status   varchar(20)  not null default 'PENDING',
    session_enrollment_status varchar(20)  not null default 'NOT_RECRUITING',
    student_capacity          bigint,
    session_fee               bigint,
    primary key (id)
);

create table enrollment
(
    id                bigint generated by default as identity,
    session_id        bigint      not null,
    user_id           bigint      not null,
    enrollment_date   timestamp   not null,
    payment_id        bigint,
    approval_status   varchar(20) not null default 'NOT_APPROVED',
    enrollment_status varchar(20) not null default 'ACTIVE',
    primary key (id)
);

create table cover_image
(
    id         bigint generated by default as identity,
    session_id bigint      not null,
    size       int         not null,
    image_type varchar(10) not null,
    width      int         not null,
    height     int         not null,
    primary key (id)
);

create table payment
(
    id         bigint generated by default as identity,
    session_id bigint    not null,
    ns_user_id bigint    not null,
    amount     bigint    not null,
    created_at timestamp not null,
    primary key (id)
);
