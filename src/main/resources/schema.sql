create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table session (
    id bigint generated by default as identity,
    course_id bigint not null,
    category varchar(4) not null,
    start_at timestamp not null,
    end_at timestamp not null,
    cover_image_file_size integer,
    cover_image_type varchar(4),
    cover_image_width integer,
    cover_image_height integer,
    status varchar(20),
    instructor_id bigint not null,
    process_status varchar(10) not null,
    recruitment_status varchar(10) not null,
    max_register_count integer,
    amount integer,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table student (
    id bigint generated by default as identity,
    session_id bigint not null,
    ns_user_id bigint not null,
    selected_status varchar(10) not null,
    approved_status varchar(10) not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table cover_image (
     id bigint generated by default as identity,
     session_id bigint not null,
     cover_image_file_size integer,
     cover_image_type varchar(4),
     cover_image_width integer,
     cover_image_height integer,
     created_at timestamp not null,
     updated_at timestamp,
     primary key (id)
);

create table instructor (
     id bigint generated by default as identity,
     user_id varchar(20) not null,
     password varchar(20) not null,
     name varchar(20) not null,
     email varchar(50),
     created_at timestamp not null,
     updated_at timestamp,
     primary key (id)
);