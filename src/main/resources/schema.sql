create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table session (
    id bigint generated by default as identity,
    title varchar(255) not null,
    session_type varchar(255),
    session_status varchar(255) default 'PREPARING',
    progress_status varchar(255) default 'PREPARING',
    recruitment_status varchar(255) default 'CLOSE',
    price bigint default 0 not null,
    max_enrollment bigint default 0 not null,
    started_at timestamp not null,
    ended_at timestamp not null,
    primary key (id)
);

create table session_enrollment
(
    user_id bigint,
    session_id bigint,
    enrollment_status varchar(255) default 'PENDING',
    primary key (session_id, user_id),
    foreign key (session_id) references session (id),
    foreign key (user_id) references ns_user (id)
);

create table cover_image (
    id bigint generated by default as identity,
    session_id bigint,
    title varchar(255) not null,
    format varchar(255) not null,
    fileSize bigint default 0 not null,
    width bigint default 0 not null,
    height bigint default 0 not null,
    primary key (id),
    foreign key (session_id) references session (id)
);
