create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    cohort bigint not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table session (
    id bigint generated by default as identity,
    course_id bigint not null,
    session_status varchar(10) not null,
    recruit_status varchar(10) not null,
    fee bigint not null default 0,
    start_date timestamp not null,
    end_date timestamp not null,
    capacity integer,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table session_cover_image (
    id bigint generated by default as identity,
    session_id bigint not null,
    cover_image_name varchar(255) not null,
    cover_image_extension varchar(10) not null,
    cover_image_width integer not null,
    cover_image_height integer not null,
    cover_image_size integer not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table session_users (
    session_id bigint not null,
    ns_user_id bigint not null,
    status varchar(10) not null,
    created_at timestamp not null,
    updated_at timestamp
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);
