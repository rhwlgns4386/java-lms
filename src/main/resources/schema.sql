create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table session (
    id bigint generated by default as identity,
    course_id bigint,
    session_state varchar(50) not null,
    cover_file_path varchar(255) not null,
    session_fee bigint default 0,
    enrollment int default 0,
    max_enrollment int not null,
    start_date timestamp not null,
    end_date timestamp not null,
    primary key (id),
    FOREIGN KEY (course_id) REFERENCES course(id)
);

alter table session add recruit_state varchar(50) default 'NOT_RECRUIT';
alter table session add selection_type varchar(50) default 'NON_SELECTION';

create table cover_file (
    id bigint generated by default as identity,
    file_path varchar(200) not null,
    session_id bigint not null,
    primary key (id),
    FOREIGN KEY (session_id) REFERENCES session(id)
);

create table ns_user (
     id bigint generated by default as identity,
     user_id varchar(20) not null,
     password varchar(20) not null,
     name varchar(20) not null,
     email varchar(50),
     created_at timestamp not null,
     updated_at timestamp,
     primary key (id)
);

create table student (
    id bigint generated by default as identity,
    enrollment_state varchar(50) not null,
    ns_user_id bigint not null,
    session_id bigint not null,

    primary key (id),
    FOREIGN KEY (ns_user_id) REFERENCES ns_user(id),
    FOREIGN KEY (session_id) REFERENCES session(id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);
